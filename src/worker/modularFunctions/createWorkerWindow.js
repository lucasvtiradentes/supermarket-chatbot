   const {join} = require('path');
   const {BrowserWindow} = require('electron');

// ####################################################################################################################################
   module.exports = createWorker;

// ####################################################################################################################################
   function createWorker() { // =========================================================================================

      // -----------------------------------------------------------------------
      const app_interface = join(__dirname, '../../../src/worker/page/index.html');

      const windowOptions = {
         width          : 800,
         height         : 600,
         title          : 'WORKER',
         show           : false,
         webPreferences : {
            nodeIntegration    : true,
            enableRemoteModule : true
         }
      };

      // -----------------------------------------------------------------------
      const window = new BrowserWindow(windowOptions);
      // window.setMenu(null);
      window.loadURL('file://' + app_interface);

      return window;
   }


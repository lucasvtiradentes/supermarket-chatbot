   const {ipcRenderer, remote} = require('electron');

// ####################################################################################################################################
   module.exports = stopLooping;

// ####################################################################################################################################

   function stopLooping() {
      remote.getGlobal('interfaceWindow').send('interface-change_bot_color', 'remove');
      ipcRenderer.send('main_show-cli-message', '\n' + '#### STOP LOOKING FOR MESSAGES ############################################################' + '\n');
      return;
   }

